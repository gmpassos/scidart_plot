  static Color hex(String value) {
    return Color(value);
  }

  static Color rgb(int r, int g, int b) {
    if (r > 255 || g > 255 || b > 255) {
      throw ArgumentError('r, g and b need to be in the rage 0-255');
    }
    var rh = r.toRadixString(16).padLeft(2, '0');
    var gh = r.toRadixString(16).padLeft(2, '0');
    var bh = r.toRadixString(16).padLeft(2, '0');

    return Color(rh + gh + bh);
  }

  static String toXml(Color color) {
    var colorStr = color != null ? color._value : Color.none._value;
    try {
      int.parse(colorStr, radix: 16);
      return '#' + colorStr;
    } on FormatException {
      return colorStr;
    }
  }
